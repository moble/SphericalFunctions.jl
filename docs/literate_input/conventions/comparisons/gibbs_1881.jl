# Gibbs (1881)

Josiah Willard Gibbs was a pioneering American physicist and mathematician, and his work on
vector analysis was influential in the development of modern vector calculus.  His 1881
pamphlet on vector analysis [Gibbs_1881](@cite) was one of the earliest texts to present
vector analysis in a form similar to what we use today.


> The letters ``i``, ``j``, ``k`` are appropriated to the designation of a *normal system of
> unit vectors*, i. e., three unit vectors, each of which is at right angles to the other
> two and determined in direction by them in a perfectly definite manner.  We shall always
> suppose that ``k`` is on the side of the ``i``-``j`` plane on which a rotation from ``i`` to
> ``j`` (through one right angle) appears counter-clock-wise. In other words, the directions
> of ``i``, ``j``, and ``k`` are to be so determined that if they be turned (remaining
> rigidly connected with each other) so that ``i`` points to the east, and ``j`` to the
> north, ``k`` will point upward.  When rectangular axes of ``X``, ``Y``, and ``Z`` are
> employed, their directions will be conformed to a similar condition, and ``i``, ``j``,
> ``k`` (when the contrary is not stated) will be supposed parallel to these axes
> respectively.


