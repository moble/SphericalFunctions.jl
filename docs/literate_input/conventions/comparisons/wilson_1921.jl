# Wilson (1929)

Wilson's "Vector analysis" [Wilson_1929](@cite) was, historically, one of the most
influential textbooks on vector analysis in the early 20th century.  It was based on the
lectures of [Gibbs](@ref Gibbs-(1881)), and it helped to popularize all aspects of what we
now understand as vector algebra — including the notion of a right-handed coordinate system.
He begins simply:

> By the ``X``-, ``Y``-, or ``Z``-axis the *positive* half of that axis is meant.  The ``X
> Y``-plane means the plane which contains the ``X``- and ``Y``-axis, i.e., the plane
> ``z=0``.

Now, he gets to the heart of it:

> In one case (Fig. 8, first part) the ``Z``-axis lies upon that side of the ``X Y``-plane
> on which rotation through a right angle from the ``Z``-axis to the ``Y``-axis appears
> *counterclockwise* or *positive* according to the convention adopted in Trigonometry.
> This relation may be stated in another form.  If the ``X``-axis be directed to the right
> and the ``Y``-axis vertically, the ``Z``-axis will be directed toward the observer. Or if
> the ``X``-axis point toward the observer and the ``Y``-axis to the right, the ``Z``-axis
> will point upward.  Still another method of statement is common in mathematical physics
> and engineering.  If a right-handed screw be turned from the ``X``-axis to the ``Y``-axis
> it will advance along the (positive) ``Z``-axis.  Such a system of axes is called
> right-handed, positive, or counterclockwise.

This is followed by a footnote given another description that is more common now:

> A convenient right-handed system and one which is always available consists of the thumb,
> first finger, and second finger of the right hand.  If the thumb and first finger be
> stretched out from the palm perpendicular to each other, and if the second finger be bent
> over toward the palm at right angles to first finger, a right-handed system is formed by
> the fingers taken in the order thumb, first finger, second finger.

I've left out some more discussion; in the above, he is just drawing the distinction between
left- and right-handed systems.  But eventually, he gets to the final statement of his
choice:

> In this book the right-handed or counterclockwise system will be invariably employed.

Finally, he makes the important connection between these vectors and Cartesian coordinates:

> *Definition:* The three letters ``𝐢``, ``𝐣``, ``𝐤`` will be reserved to denote three
> vectors of unit length drawn respectively in the directions of the ``X``-, ``Y``-, and
> ``Z``-axes of a right-handed rectangular system.
>
> In terms of these vectors, any vector may be expressed as
> ```math
> 𝐫 = x 𝐢 + y 𝐣 + z 𝐤.
> ```
> The coefficients ``x``, ``y``, ``z`` are the ordinary Cartesian codrdinates of the
> terminus of ``𝐫`` if its origin be situated at the origin of coördinates.  The components
> of ``𝐫`` parallel to the ``X``-, ``Y``-, and ``Z``-axes are respectively
> ```math
> x 𝐢, y 𝐣, z 𝐤.
> ```
> The rotations about ``𝐢`` from ``𝐣`` to ``𝐤``, about ``𝐣`` from ``𝐤`` to ``𝐢``, and
> about ``𝐤`` from ``𝐢`` to ``𝐣`` are all positive.

